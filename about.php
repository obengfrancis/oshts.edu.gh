<h4>OUR HISTORY</h4></center>
<p>From a humble beginning with 33 boys of the then Government Secondary Technical School under its first headmaster, Thomas Baidoo, the science bias institution has grown in leaps and bounds to become arguably one of Ghana’s best technical institution.</p>
<p>Currently, with a student population of 2,828, which has been largely influenced by educational reforms in the 1990s to embrace girls, its main function of running a science and technical education appears to have been enhanced with the introduction of the humanities.</p>
<p>With the exception of visual arts, the school now runs all second cycle courses, including a Vocational/Technical (VOTEC) programme, designed to equip students with the requisite employable skills to provide the middle manpower needs of the country.</p>
<p>Indeed, by 1970, the first batch of Ordinary Level Certificate Examination (O’Level) students had been introduced to the school and by 1977, the school had been given a sixth form status to run side by side with the technical and science programmes. </p>
<p>Obuasi Sec. Tech may not be regarded as one of the country’s flamboyant second cycle institutions and may not be one of the well-known institutions outside the Ashanti Region.</p>
<p>But just as every institution is unique, Obuasi Secondary Technical can hold its own against any institution in Ghana, especially when it comes to producing the country’s best engineers and sports personalities.</p>
