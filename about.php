  <center><h3>Two</h3></center>
  <p>
  Obuasi Senior High Technical School has produced and continues to produce people of high caliber who hold various responsible positions in the country.</p>

<p>On the whole, the academic performance has been quite good. The WASSCE results of 2011 were unprecedented since about 75% of the candidates qualified into tertiary institutions. We are however, working hard to improve upon the academic performance.</p>

<p>Many products of the school have gained admission into tertiary institutions in the country to pursue courses in Science, Engineering, Education and of late in Business and Arts.</p>

<p>The introduction of new programmes such as Business, Agriculture and General Arts has led to significant improvement in the total number of quality grades especially in 2002. The first batch of Automotive Engineering, Mechanical Engineering, Electrical  Installation, Building and Construction and Carpentry and Joinery courses in the new Vocational Technical  Centre established in recent years came out with encouraging performance in their final exams especially  in the practical.</p>

<p>In the past five years the school has recorded 100% in the final WASSCE conducted by WAEC. A significant and unprecedented achievement was recorded by a female science student who made a total of 7 A1s.</p>

<p>The school was adjudged the best in the Secondary/Technical category in Ashanti Region and  awarded a Certificate of Honour during the maiden edition of the National Best School Awards ceremony held in Tamale on 5th October, 2015.</p>

<p>Over the years Obuasi Senior High Technical School has been counted among the big shots in the region when it comes to sports achievement. The sports Department has maintained this enviable position as sports boys/girls continue to excel in football, volleyball, athletics etc. at both Zonal and Regional levels</p>

<p>Apart from our splendid performances in athletics and other sporting disciplines at various levels, the school has produced outstanding footballers at national level. Players like Jonathan Mensah, Daniel Opare Tawiah, Mubarak Wakasu and Edwin Gyimah are some of the finest players in our national team, the Black Stars. Opoku Agyemang and a host of others have also played for the Black Stars and other national teams at different levels.
